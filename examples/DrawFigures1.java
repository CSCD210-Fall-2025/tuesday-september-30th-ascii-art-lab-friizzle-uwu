/**
 * A monolithic program that prints three ASCII art figures: a diamond, an
 * X shape, and a rocket.  This example corresponds to the starting point
 * before introducing static methods.
 */
public class DrawFigures1 {
    public static void main(String[] args) {
        // Diamond figure
        System.out.println("    /\\");
        System.out.println("   /  \\");
        System.out.println("  /    \\");
        System.out.println("  \\    /");
        System.out.println("   \\  /");
        System.out.println("    \\\\/");
        System.out.println();
        System.out.println("  \\    /");
        System.out.println("   \\  /");
        System.out.println("    \\\\/");
        System.out.println("    /\\");
        System.out.println("   /  \\");
        System.out.println("  /    \\");
        System.out.println();
        System.out.println("    /\\");
        System.out.println("   /  \\");
        System.out.println("  /    \\");
        System.out.println("+------+ ");
        System.out.println("|      |");
        System.out.println("|      |");
        System.out.println("+------+ ");
        System.out.println("|United|");
        System.out.println("|States|");
        System.out.println("+------+ ");
        System.out.println("|      |");
        System.out.println("|      |");
        System.out.println("+------+ ");
        System.out.println("    /\\");
        System.out.println("   /  \\");
        System.out.println("  /    \\");
    }
}